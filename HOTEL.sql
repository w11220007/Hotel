-- Create the HOTEL_DB
DROP DATABASE IF EXISTS HOTEL_DB;
CREATE DATABASE IF NOT EXISTS HOTEL_DB;
USE HOTEL_DB;

-- Create the table HOTEL
CREATE TABLE IF NOT EXISTS HOTEL(
  HID INT NOT NULL AUTO_INCREMENT,
  HName VARCHAR(64) NOT NULL,
  Address VARCHAR(128) NOT NULL,
  Hotline VARCHAR(64) NOT NULL,
  Services VARCHAR(64) DEFAULT '123456789',
  -- 1: Restaurant, 2: Conference, 3: Bar, 4: Spa, 5: Fitness center, 6: Golf, 7: Tennis, 8: Laundry, 9: Shuttle
  PRIMARY KEY (HID)
  );

  -- Create the table ROOM
  CREATE TABLE IF NOT EXISTS ROOM(
	RID INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (RID),
    HID INT NOT NULL,
	FOREIGN KEY (HID) REFERENCES HOTEL(HID),
    ROOM_TYPE VARCHAR(1) NOT NULL, -- 1: Standard, 2: Superior, 3: Deluxe, 4: Suite
    ROOM_CAPACITY VARCHAR(1) NOT NULL, -- 1: Single, 2: Double, 3: Trio, 4: Family
    BASE_PRICE FLOAT NOT NULL,
    Services VARCHAR(3) NOT NULL -- 1: Wifi, 2: Separate Rooms, 4: Pet care
  );

CREATE TABLE IF NOT EXISTS USER(
ID INT NOT NULL AUTO_INCREMENT,
EMAIL VARCHAR(255),
FIRST_NAME VARCHAR(64),
LAST_NAME VARCHAR(64),
PASSWORD VARCHAR(255),
PRIMARY KEY CLUSTERED (ID ASC),
PHONE_NUMBER VARCHAR(64),
DATE_OF_REGISTRATION DATE DEFAULT (CURDATE())
);

-- Create the table HOTEL_BOOKING
CREATE TABLE IF NOT EXISTS HOTEL_BOOKING(
BOOKING_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
ID INT NOT NULL,
FOREIGN KEY (ID) REFERENCES USER(ID),
RID INT NOT NULL,
FOREIGN KEY (RID) REFERENCES ROOM(RID),
FINAL_PRICE float not null,
BOOKING_DATE date not null default (curdate()),
CHECK_IN_DATE date not null,
CHECK_OUT_DATE date not null,
payment_status varchar(64) not null default 'pending'
);

-- Create the table for admins
CREATE TABLE IF NOT EXISTS ADMIN(
AID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
ADMIN_NAME VARCHAR(30) NOT NULL,
ADMIN_EMAIL VARCHAR(40),
PRIVILEDGE_LEVEL INT NOT NULL
);

-- Create the table for vouchers
CREATE TABLE IF NOT EXISTS VOUCHERS(
voucher varchar(64) not null primary key,
discount_percentage integer,
discount_amount integer
);


